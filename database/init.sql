DROP TABLE IF EXISTS sales_features;

CREATE TABLE sales_features (
    item_id VARCHAR(50),
    dept_id VARCHAR(50),
    cat_id VARCHAR(50),
    store_id VARCHAR(50),
    state_id VARCHAR(10),
    date DATE,
    sales FLOAT,
    sell_price FLOAT,
    weekday VARCHAR(20),
    month INT,
    year INT,
    snap INT,
    is_weekend INT,
    temperature_2m_max FLOAT,
    temperature_2m_min FLOAT,
    temperature_2m_mean FLOAT,
    apparent_temperature_max FLOAT,
    apparent_temperature_min FLOAT,
    precipitation_sum FLOAT,
    rain_sum FLOAT,
    snowfall_sum FLOAT,
    wind_speed_10m_max FLOAT,
    weather_code INT,
    is_rainy INT,
    is_snowy INT,
    is_hot INT,
    is_cold INT,
    day_of_month INT,
    day_of_year INT,
    week_of_year INT,
    quarter INT,
    daily_total_sales FLOAT,
    cat_daily_sales FLOAT,
    dept_daily_sales FLOAT,
    item_daily_share FLOAT,
    lag_1 FLOAT,
    lag_7 FLOAT,
    rolling_mean_7 FLOAT,
    rolling_std_7 FLOAT,
    sales_diff_1 FLOAT,
    sales_diff_7 FLOAT,
    price_change_1 FLOAT,
    price_change_7 FLOAT,
    sales_pct_change_1 FLOAT,
    price_pct_change_1 FLOAT,
    is_zero_sales INT,
    lag_zero_1 INT,
    has_event_1 INT,
    has_event_2 INT,
    has_any_event INT,
    expected_daily_demand FLOAT,
    safety_stock FLOAT,
    reorder_point_7d FLOAT
);